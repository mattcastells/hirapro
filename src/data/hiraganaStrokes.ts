const handakutenExtraStroke: CharacterStroke = [[85, 10], [85, 11]];

/**
 * Get stroke data for a hiragana kana character.
 * Returns null if no data is available (e.g. combo characters).
 */
export function getStrokeGuide(kana: string): CharacterStroke[] | null {
  if (kana.length !== 1) {
    return null;
  }

  const direct = baseStrokes[kana];

  if (direct) {
    return direct;
  }

  const dakutenBase = dakutenMap[kana];

  if (dakutenBase) {
    const base = baseStrokes[dakutenBase];

    if (base) {
      return [...base, ...dakutenExtraStrokes];
    }
  }

  const handakutenBase = handakutenMap[kana];

  if (handakutenBase) {
    const base = baseStrokes[handakutenBase];

    if (base) {
      return [...base, handakutenExtraStroke];
    }
  }

  return null;
}

/**
 * Get the expected stroke count for a hiragana character.
 */
export function getExpectedStrokeCount(kana: string): number | null {
  const guide = getStrokeGuide(kana);
  return guide ? guide.length : null;
}
